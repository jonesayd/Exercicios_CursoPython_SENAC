#15. Crie um programa que leia o número de dias trabalhados em um mês e mostre o salário de um funcionário, 
# sabendo que ele trabalha 8 horas por dia e ganha R$25 por hora trabalhada.

print("----------------------------------------------")
print("          DIAS TRABALHADOS - SALARIO          ")
print("----------------------------------------------")

dias_trab=int(input("Digite a quantidade de dias trabalhados: "))

print("----------------------------------------------")

salario=dias_trab*200
print("----------------------------------------------")
print("Considerando os", dias_trab, " dias trabalhados")
print("O salario percebido no mes foi de: ", salario)
print("----------------------------------------------")